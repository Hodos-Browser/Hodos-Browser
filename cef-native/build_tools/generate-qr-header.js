#!/usr/bin/env node
// Generates QRScannerScript.h from jsqr.min.js + qr-scanner-logic.js
// Splits into multiple string literals to work around MSVC C2026 (16380 char limit).
// Run: node generate-qr-header.js

const fs = require('fs');
const path = require('path');

const jsqrPath = path.join(__dirname, 'jsqr.min.js');
const logicPath = path.join(__dirname, 'qr-scanner-logic.js');
const outputPath = path.join(__dirname, '..', 'include', 'core', 'QRScannerScript.h');

const jsqr = fs.readFileSync(jsqrPath, 'utf8');
const logic = fs.readFileSync(logicPath, 'utf8');

// Combine: jsQR library first (defines jsQRLib global), then scanner logic
const combined = jsqr + '\n' + logic;

// MSVC limits string literals to 16380 characters. Split into chunks.
const CHUNK_SIZE = 15000;
const chunks = [];
for (let i = 0; i < combined.length; i += CHUNK_SIZE) {
    chunks.push(combined.slice(i, i + CHUNK_SIZE));
}

// Escape each chunk for a C++ regular string literal (not raw).
// We must escape: backslash, double-quote, newline, carriage return, tab, null
function escapeForCppString(str) {
    return str
        .replace(/\\/g, '\\\\')
        .replace(/"/g, '\\"')
        .replace(/\n/g, '\\n')
        .replace(/\r/g, '\\r')
        .replace(/\t/g, '\\t')
        .replace(/\0/g, '\\0');
}

const escapedChunks = chunks.map(c => `"${escapeForCppString(c)}"`);

const header = `#pragma once

// AUTO-GENERATED by build_tools/generate-qr-header.js
// Do not edit manually. Regenerate with:
//   cd cef-native/build_tools
//   node generate-qr-header.js
//
// Contains: jsQR library (minified) + DOM scanner + BSV pattern filter
// Injected into page context on demand to scan for QR codes.
//
// Split into ${chunks.length} string literals to work around MSVC C2026 (16380 char limit).

static const char QR_SCANNER_SCRIPT[] =
${escapedChunks.join('\n')};
`;

fs.writeFileSync(outputPath, header, 'utf8');
const sizeKB = (Buffer.byteLength(header) / 1024).toFixed(1);
console.log(`Generated ${outputPath} (${sizeKB} KB, ${chunks.length} chunks)`);
